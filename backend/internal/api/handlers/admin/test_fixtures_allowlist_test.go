// TestAllowlistCoversAllUserFKTables queries information_schema for every
// table with a foreign key column pointing at users, and asserts the table
// is either covered by the reset allowlist or explicitly skipped.
func (s *TestFixturesAllowlistContractSuite) TestAllowlistCoversAllUserFKTables() {
	db := s.deps.db

	userTables, err := tablesWithUsersFK(db)
	s.Require().NoError(err)
	s.Require().NotEmpty(userTables, "information_schema query returned no user-FK tables — query or schema may have drifted")

	covered := make(map[string]bool, len(testFixtureAllowlist))
	for _, scope := range testFixtureAllowlist {
		// The allowlist's display names mostly match physical tables. The
		// one exception is the virtual `pending_shows` scope, which covers
		// the `shows` table (filtered). Map it explicitly here.
		if scope.displayName == "pending_shows" {
			covered["shows"] = true
			continue
		}
		covered[scope.displayName] = true
	}

	// collection_items' FK is `added_by_user_id` — the contract check should
	// accept that. Handled by direct inclusion in the allowlist.
	var missing []string
	for _, tbl := range userTables {
		if covered[tbl] {
			continue
		}
		if _, ok := testFixturesAllowlistContractSkips[tbl]; ok {
			continue
		}
		missing = append(missing, tbl)
	}

	if len(missing) > 0 {
		sort.Strings(missing)
		s.T().Errorf(
			"allowlist drift: the following tables have a FK to users but are neither in testFixtureAllowlist nor in testFixturesAllowlistContractSkips:\n  %s\n\nAdd each to the allowlist OR to the skip map with a justifying comment. See test_fixtures_allowlist_test.go.",
			strings.Join(missing, "\n  "),
		)
	}
}

// tablesWithUsersFK returns the set of tables (other than `users` itself)
// that have a foreign key column referencing `users`.
func tablesWithUsersFK(db *gorm.DB) ([]string, error) {
	rows, err := db.Raw(`
		SELECT DISTINCT tc.table_name
		FROM information_schema.table_constraints tc
		JOIN information_schema.constraint_column_usage ccu
		  ON tc.constraint_name = ccu.constraint_name
		 AND tc.table_schema = ccu.table_schema
		WHERE tc.constraint_type = 'FOREIGN KEY'
		  AND ccu.table_name = 'users'
		  AND tc.table_schema = current_schema()
		ORDER BY tc.table_name
	`).Rows()
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	var tables []string
	for rows.Next() {
		var t string
		if err := rows.Scan(&t); err != nil {
			return nil, err
		}
		if t == "users" {
			continue
		}
		tables = append(tables, t)
	}
	return tables, rows.Err()
}
