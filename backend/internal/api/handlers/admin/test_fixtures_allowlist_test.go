// TestFixturesAllowlistContractSuite is a CI-enforced check that every table
// with a foreign key to `users` is either (a) in the reset endpoint's
// allowlist, (b) in a documented skip set, or (c) behind a mapped scope
// whose predicate joins through to a user column we do cover.
//
// Why this matters: the reset endpoint's value depends on being exhaustive.
// If a new user-owned table ships and nobody updates the allowlist, tests
// silently leak state for weeks and we only notice when a flake reproduces
// across a long delay. This contract fails CI on the PR that introduces the
// drift.
//
// If you add a new user-FK table, choose one:
//   - Add it to `testFixtureAllowlist` (so tests can reset it), OR
//   - Add it to `testFixturesAllowlistContractSkips` below with a comment
//     explaining why it should NEVER be reset between E2E runs (e.g., audit
//     logs we intentionally preserve, or internal tables whose lifecycle is
//     owned by the backend itself).
type TestFixturesAllowlistContractSuite struct {
	suite.Suite
	deps *handlerIntegrationDeps
}

func TestTestFixturesAllowlistContractSuite(t *testing.T) {
	suite.Run(t, new(TestFixturesAllowlistContractSuite))
}

func (s *TestFixturesAllowlistContractSuite) SetupSuite() {
	s.deps = setupHandlerIntegrationDeps(s.T())
}

